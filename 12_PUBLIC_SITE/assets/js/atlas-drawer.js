/* Atlas drawer — the gitbook tree on every page.
   Self-contained: injects a ☰ Atlas button (bottom-right) that slides in a
   panel with full-corpus search and the collapsible site tree, fed by
   /atlas/site_index.json (generated by build_atlas_index.py). No external
   resources; Skyzai palette inline. */
(function () {
  "use strict";
  if (window.__atlasDrawer) return;
  window.__atlasDrawer = true;

  var GOLD = "#FFEB3B", INK = "#050505", CREAM = "#FFFDE7";

  var css = [
    "#atlas-fab{position:fixed;right:18px;bottom:18px;z-index:9000;background:" + INK + ";color:" + CREAM + ";",
    "border:1px solid rgba(255,235,59,.55);border-radius:999px;padding:10px 16px;font:600 13px/1 'Roboto','Noto Sans',sans-serif;",
    "letter-spacing:.08em;cursor:pointer;opacity:.92}",
    "#atlas-fab:hover{background:" + GOLD + ";color:" + INK + "}",
    "#atlas-panel{position:fixed;top:0;right:0;bottom:0;width:min(380px,92vw);z-index:9001;background:rgba(5,5,5,.97);",
    "border-left:1px solid rgba(255,235,59,.35);clip-path:inset(0 0 0 100%);opacity:0;visibility:hidden;pointer-events:none;",
    "transition:clip-path .22s ease,opacity .22s ease,visibility 0s linear .22s;",
    "display:flex;flex-direction:column;font-family:'Roboto','Noto Sans',sans-serif;color:" + CREAM + ";backdrop-filter:blur(6px)}",
    "#atlas-panel.open{clip-path:inset(0);opacity:1;visibility:visible;pointer-events:auto;transition:clip-path .22s ease,opacity .22s ease}",
    "#atlas-head{display:flex;gap:8px;align-items:center;padding:14px 14px 10px}",
    "#atlas-head b{color:" + GOLD + ";font-size:13px;letter-spacing:.14em}",
    "#atlas-close{margin-left:auto;background:none;border:0;color:" + CREAM + ";font-size:18px;cursor:pointer;opacity:.7}",
    "#atlas-close:hover{opacity:1;color:" + GOLD + "}",
    "#atlas-q{margin:0 14px 10px;padding:9px 12px;background:#111;border:1px solid #333;border-radius:8px;color:" + CREAM + ";",
    "font:400 13px/1.2 'Roboto Mono',monospace;outline:none}",
    "#atlas-q:focus{border-color:" + GOLD + "}",
    "#atlas-tree{overflow-y:auto;padding:0 8px 20px;flex:1}",
    "#atlas-tree details{margin:2px 6px}",
    "#atlas-tree summary{cursor:pointer;padding:6px 8px;font-size:12.5px;letter-spacing:.06em;color:#cfcab0;list-style:none}",
    "#atlas-tree summary::before{content:'▸ ';color:" + GOLD + "}",
    "#atlas-tree details[open]>summary::before{content:'▾ '}",
    "#atlas-tree a{display:block;padding:5px 10px 5px 24px;font-size:13px;color:" + CREAM + ";text-decoration:none;",
    "border-left:1px solid #2a2a2a;margin-left:12px;opacity:.85}",
    "#atlas-tree a:hover{color:" + GOLD + ";opacity:1}",
    "#atlas-tree a.atlas-here{color:" + GOLD + ";border-left-color:" + GOLD + ";opacity:1}",
    "#atlas-count{padding:4px 22px 10px;font:400 11px/1 'Roboto Mono',monospace;color:#8a8568}",
    "@media(max-width:680px){body.dimension-page #atlas-fab{display:none}}"
  ].join("");

  var style = document.createElement("style");
  style.textContent = css;
  document.head.appendChild(style);

  var fab = document.createElement("button");
  fab.id = "atlas-fab";
  fab.type = "button";
  fab.setAttribute("aria-label", "Open the corpus atlas");
  fab.textContent = "☰ ATLAS";
  document.body.appendChild(fab);

  var panel = document.createElement("aside");
  panel.id = "atlas-panel";
  panel.setAttribute("aria-label", "Corpus atlas tree");
  panel.innerHTML =
    '<div id="atlas-head"><b>EMERGENTISM · ATLAS</b>' +
    '<button id="atlas-close" type="button" aria-label="Close">×</button></div>' +
    '<input id="atlas-q" type="search" placeholder="search the corpus…" autocomplete="off">' +
    '<div id="atlas-count"></div><nav id="atlas-tree"></nav>';
  document.body.appendChild(panel);

  var data = null;
  var treeEl = panel.querySelector("#atlas-tree");
  var countEl = panel.querySelector("#atlas-count");
  var qEl = panel.querySelector("#atlas-q");
  var here = location.pathname.replace(/index\.html$/, "");

  function render(filter) {
    if (!data) return;
    var q = (filter || "").trim().toLowerCase();
    var shown = 0;
    treeEl.textContent = "";
    data.tree.forEach(function (sec) {
      var det = document.createElement("details");
      var sum = document.createElement("summary");
      sum.textContent = sec.label;
      det.appendChild(sum);
      var any = false;
      sec.pages.forEach(function (p) {
        if (q && p.title.toLowerCase().indexOf(q) === -1 && p.href.toLowerCase().indexOf(q) === -1) return;
        var a = document.createElement("a");
        a.href = p.href;
        a.textContent = p.title;
        if (p.href === here) a.className = "atlas-here";
        det.appendChild(a);
        any = true;
        shown += 1;
      });
      if (!any) return;
      det.open = !!q || sec.pages.some(function (p) { return p.href === here; });
      treeEl.appendChild(det);
    });
    if (!treeEl.firstChild) {
      var fallback = document.createElement("a");
      fallback.href = "/atlas/";
      fallback.textContent = "no match — open the full atlas";
      treeEl.appendChild(fallback);
    }
    countEl.textContent = q ? (shown + " / " + data.total + " pages") : (data.total + " pages · " + data.tree.length + " sections");
  }

  function load() {
    if (data) return;
    fetch("/atlas/site_index.json").then(function (r) { return r.json(); }).then(function (j) {
      data = j; render("");
    }).catch(function () {
      treeEl.textContent = "";
      var a = document.createElement("a");
      a.href = "/atlas/";
      a.textContent = "open the atlas page →";
      treeEl.appendChild(a);
    });
  }

  function openPanel() { panel.classList.add("open"); load(); qEl.focus(); }
  function closePanel() { panel.classList.remove("open"); }

  fab.addEventListener("click", openPanel);
  panel.querySelector("#atlas-close").addEventListener("click", closePanel);
  qEl.addEventListener("input", function () { render(qEl.value); });
  document.addEventListener("keydown", function (e) {
    if (e.key === "Escape") closePanel();
  });
})();
