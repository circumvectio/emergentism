#!/usr/bin/env python3
"""
compile_state.py — Auto-generate EMERGENTISM_ORG/11_UPLINK/09_STATE.md from live organism data.

Reads:
  - SKYZAI_ORG/P-SCORES.md (canonical health)
  - SKYZAI_ORG/02_ORGANS/*/  (file counts)
  - 03_VENTURES/*/ (file counts)
  - git status (if available)

Writes:
  - EMERGENTISM_ORG/11_UPLINK/09_STATE.md

Run: python3 EMERGENTISM_ORG/09_TOOLS/compile_state.py
"""

import os
import re
import subprocess
from datetime import datetime, timezone
from pathlib import Path

# Paths relative to repo root
REPO_ROOT = Path(__file__).resolve().parent.parent.parent
P_SCORES_PATH = REPO_ROOT / "02_ORGANISM" / "P-SCORES.md"
ORGANS_DIR = REPO_ROOT / "02_ORGANISM" / "02_ORGANS"
ENTITIES_DIR = REPO_ROOT / "02_ORGANISM" / "01_ENTITIES"
OUTPUT_PATH = REPO_ROOT / "03_UPLINK" / "09_STATE.md"

# Known deployments (update as things go live)
